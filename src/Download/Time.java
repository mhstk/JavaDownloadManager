package Download;

public class Time {
    int hour;
    int min;
    int day;
    int month;
    int year;

    public void setTime(int hour, int min, int day, int month, int year) {
        this.hour = hour;
        this.min = min;
        this.day = day;
        this.month = month;
        this.year = year;
    }
}
